public class Cities{
    private void miami;
    private void newYork;
    private void chicago;
    private void houston;
    private void losAngeles;

    public void getMiami() {
        return miami;
    }

    public void setMiami(void miami) {
        this.miami = miami;
    }

    public void getNewYork() {
        return newYork;
    }

    public void setNewYork(void newYork) {
        this.newYork = newYork;
    }

    public void getChicago() {
        return chicago;
    }

    public void setChicago(void chicago) {
        this.chicago = chicago;
    }

    public void getHouston() {
        return houston;
    }

    public void setHouston(void houston) {
        this.houston = houston;
    }

    public void getLosAngeles() {
        return losAngeles;
    }

    public void setLosAngeles(void losAngeles) {
        this.losAngeles = losAngeles;
    }
}